CREATE SEQUENCE SEQ_USER;

CREATE TABLE TBL_USER(
	USER_NUMBER NUMBER, 
	USER_ID VARCHAR2(100),
	USER_PW VARCHAR2(100),
	USER_NAME VARCHAR2(100),
	USER_AGE NUMBER, 
	USER_GENDER CHAR(1) DEFAULT 'M' NOT NULL, 
	USER_BIRTH DATE, 
	CONSTRAINT PK_USER PRIMARY KEY(USER_NUMBER)
);

